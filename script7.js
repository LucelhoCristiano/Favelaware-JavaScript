let listaDeNome = 'Lucas'

switch (listaDeNome) {
    case  'Lucas':
        console.log ('nome Lucas esta na lista')
        break;
    case  'Pedro':
        console.log('nome Pedro esta na lista')
    case 'larissa':
        console.log('nome Larissa esta na lista')
    case 'Joao':
        console.log('nome Joao esta na lista')
    case 'Ana':
        console.log('nome Ana esta na lista')
        break;
       default :
       console.log('Item invalido')
       break
}

